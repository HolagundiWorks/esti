/**
 * Sync frontend wiki markdown → backend ESTI product knowledge.
 * Run: node frontend/scripts/sync-wiki-knowledge.mjs
 * Wire into CI/prebuild when wiki content changes.
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const wikiDir = join(root, "src", "content", "wiki");
const outPath = join(root, "..", "backend", "src", "lib", "ai", "wiki-knowledge.generated.ts");

function parseFrontmatter(raw) {
  const m = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?([\s\S]*)$/.exec(raw);
  if (!m) return { data: {}, body: raw };
  const data = {};
  for (const line of m[1].split(/\r?\n/)) {
    const i = line.indexOf(":");
    if (i === -1) continue;
    const k = line.slice(0, i).trim();
    let v = line.slice(i + 1).trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
    data[k] = v;
  }
  return { data, body: (m[2] ?? "").trim() };
}

const pages = readdirSync(wikiDir)
  .filter((f) => f.endsWith(".md"))
  .map((f) => {
    const raw = readFileSync(join(wikiDir, f), "utf8");
    const { data, body } = parseFrontmatter(raw);
    return {
      slug: data.slug || f.replace(/\.md$/, ""),
      title: data.title || f,
      section: data.section || "",
      order: Number(data.order) || 99,
      body,
    };
  })
  .sort((a, b) => a.order - b.order);

const sections = pages
  .map((p) => `### ${p.title} (aorms.in/wiki/${p.slug})\n\n${p.body}`)
  .join("\n\n---\n\n");

const header = `/**
 * AUTO-GENERATED by frontend/scripts/sync-wiki-knowledge.mjs
 * Source: frontend/src/content/wiki/*.md — do not edit by hand.
 * Regenerate after wiki changes so ESTI (Ask ESTI + landing AI) stays aligned.
 */
`;

const body = `${header}
export const WIKI_PRODUCT_KNOWLEDGE = \`\\
## Official AORMS Wiki (canonical product documentation)

Public URL: https://aorms.in/wiki

${sections.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${")}
\`;
`;

writeFileSync(outPath, body, "utf8");
console.log(`[sync-wiki-knowledge] wrote ${pages.length} wiki page(s) → ${outPath}`);
